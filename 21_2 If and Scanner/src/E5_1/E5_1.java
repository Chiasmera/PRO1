package E5_1;

public class E5_1 {

    public void checkIntPositive (int input) {
        if (input > 0) {
            System.out.println("Input is positive");
        } else if (input ==0) {
            System.out.println("Input is zero");
        } else {
            System.out.println("Input is negative");
        }
    }

}
